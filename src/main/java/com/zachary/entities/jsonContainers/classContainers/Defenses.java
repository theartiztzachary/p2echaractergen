package com.zachary.entities.jsonContainers.classContainers;

public class Defenses {
    private int heavy;
    private int light;
    private int medium;
    private int unarmed;

    public int getHeavy() {
        return heavy;
    }

    public void setHeavy(int heavy) {
        this.heavy = heavy;
    }

    public int getLight() {
        return light;
    }

    public void setLight(int light) {
        this.light = light;
    }

    public int getMedium() {
        return medium;
    }

    public void setMedium(int medium) {
        this.medium = medium;
    }

    public int getUnarmed() {
        return unarmed;
    }

    public void setUnarmed(int unarmed) {
        this.unarmed = unarmed;
    }
}
