package com.zachary.entities.jsonContainers.classContainers;

public class Attacks {
    private int advanced;
    private int martial;
    private Other other;
    private int simple;
    private int unarmed;

    public int getAdvanced() {
        return advanced;
    }

    public void setAdvanced(int advanced) {
        this.advanced = advanced;
    }

    public int getMartial() {
        return martial;
    }

    public void setMartial(int martial) {
        this.martial = martial;
    }

    public Other getOther() {
        return other;
    }

    public void setOther(Other other) {
        this.other = other;
    }

    public int getSimple() {
        return simple;
    }

    public void setSimple(int simple) {
        this.simple = simple;
    }

    public int getUnarmed() {
        return unarmed;
    }

    public void setUnarmed(int unarmed) {
        this.unarmed = unarmed;
    }
}
